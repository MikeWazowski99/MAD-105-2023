fun main(){
    val myByte: Byte = 9
    val myShort: Short = 90
    val myInt: Int = 900
    val myLong: Long = 900000
    val myFloat: Float = 3.14F
    val myDouble: Double = 31.415
    val myBoolean: Boolean = true
