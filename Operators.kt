fun main() {
    var a = 9
    var b = 11
    var c = 20

    // First Operator
    var ans1 = a + b
    println(ans1)

    var x = 7 + 7
    println(x)

    // Second Operator
    var ans2 = a >= c
    println(ans2)

    // Third Operator
    var ans3 = 5 % 5
    println(ans3)

    // Fourth Operator
    var ans4 = 26
    ans4 -= 6
    println(ans4)

    // Fifth Operator
    var ans5 = 11
    ans5++
    println(ans5)
}